    private void showHoadon() throws SQLException{
        List<Hoa_don> hoadonList = null;
        try {
             hoadonList = Hoa_donDao.findAll();
        } catch (ClassNotFoundException ex) {
         Logger.getLogger(HoaDonJPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        tabModel.setRowCount(0);
        
        hoadonList.forEach((hd) -> {
            tabModel.addRow(new Object[] {tabModel.getRowCount()+1, hd.getMahd(), hd.getMakh(), hd.getManv(), hd.getMadv(), hd.getTongtien(), hd.getNgaythanhtoan(), hd.getHinhthucthanhtoan() });
        });
    }
