    private void btnTimkiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimkiemActionPerformed
        // TODO add your handling code here:
         if(txtMahd.getText().equals(""))
        {
            JOptionPane.showMessageDialog(this, "can nhap ma hoa can tim!!!");
            return;
        }
        
        try{
            Hoa_donDao dao = new Hoa_donDao();
            
            Hoa_don hd = dao.findID(txtMahd.getText());
            
            if(hd !=null)
            {
                txtMakh.setText(hd.getMakh());
                txtManv.setText(hd.getManv());
                txtMadv.setText(hd.getMadv());
                String tt=String.valueOf(hd.getTongtien());
                txtTongtien.setText(tt);
                SimpleDateFormat ngaytt= new SimpleDateFormat("yyyy-MM-dd");
                Date date= ngaytt.parse(hd.getNgaythanhtoan());
                dcNgaythanhtoan.setDate(date);
                cmbHinhttt.setSelectedItem(hd.getHinhthucthanhtoan());
            }else
                JOptionPane.showMessageDialog(this, "khong tim thay ma hoa don!!!");
        }catch(Exception ex)
            {
                JOptionPane.showMessageDialog(this, "Error: "+ex.getMessage());
                ex.printStackTrace();
            }
    }//GEN-LAST:event_btnTimkiemActionPerformed

